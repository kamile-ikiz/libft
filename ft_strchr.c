char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*a;

	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
		{
			a = (char *)s + i;
			return (a);
		}
		i++;
	}
	if (s[i] == '\0' && (char)c == '\0')
		return ((char *)s + i);
	return (0);
}
