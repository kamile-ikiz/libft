char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*a;

	i = 0;
	while (s[i])
		i++;
	while (i >= 0)
	{
		if (s[i] == (unsigned char)c)
		{
			a = (char *)&s[i];
			return (a);
		}
		i--;
	}
	return (0);
}
