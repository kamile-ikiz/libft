#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned char	i;

	i = 0;
	while (((unsigned char)s1[i] || (unsigned char)s2[i]) && (i < n))
	{
		if ((unsigned char)s1[i] != (unsigned char)s2[i] && (i < n))
		{
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
		}
		i++;
	}
	return (0);
}
